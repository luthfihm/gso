    <br />
    <p align="justify">
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
    Seiring dengan berkembangnya zaman, ilmu pengetahuan khususnya sains juga
makin berkembang. Kebutuhan manusia semakin kompleks dengan adanya perkembanganm sains. Kebutuhan manusia tersebut dapat terpenuhi dengan lebih praktis dan efisien. Tidak hanya diterapkan secara teori saja, sains juga diterapkan dalam berbagai praktek di kehidupan sehari-hari. Selain itu manusia juga mulai menciptakan teknologi berbasis sains yang bermanfaat untuk meningkatkan taraf hidup mereka.<br /><br />
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Mengingat betapa pentingnya sains dalam setiap aspek kehidupan manusia, OSIS SMA Negeri 3 Semarang bersama dengan Menuju Olimpiade Sains Indonesia (MOSI) SMA Negeri 3 Semarang merasa perlu mengadakan sebuah kegiatan yang dapat menumbuhkan minat para pelajar khususnya siswa-siswi SMP terhadap bidang sains. Salah satunya adalah dengan mengadakan <b>Olimpiade Sains antar SMP tingkat Jawa-Bali</b> yang bertajuk <b>Ganesha Science Olympiad.</b><br /><br />

GSO mencakup 4 bidang pelajaran antara lain <b>Biologi, Fisika, Matematika, Komputer</b> dengan hadiah sebagai berikut :
